import UIKit

extension UIColor {
    class func vallientPrimaryColor() -> UIColor {
        return UIColor(red: 26/255, green: 163/255, blue: 255/255, alpha: 1.0)
    }
    
    class func vallientSecondaryColor() -> UIColor {
        return UIColor(red: 128/255, green: 204/255, blue: 255/255, alpha: 1.0)
    }
    
    class func vallientTextColor() -> UIColor {
        return UIColor(red: 38/255, green: 38/255, blue: 38/255, alpha: 1.0)
    }
    
    class func vallientLinkColor() -> UIColor {
        return UIColor(red: 63/255, green: 91/255, blue: 225/255, alpha: 1)
    }
}